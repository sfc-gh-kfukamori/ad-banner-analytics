def generate_pdfs():
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    for report in REPORTS:
        pdf = JapanesePDF()
        pdf.set_auto_page_break(auto=True, margin=15)
        pdf.add_title(report["title"])
        for heading, body in report["sections"]:
            pdf.add_section(heading)
            pdf.add_body(body)
        filepath = os.path.join(OUTPUT_DIR, report["filename"])
        pdf.output(filepath)
        print(f"Generated: {filepath}")
    print(f"\nAll {len(REPORTS)} PDFs generated in {OUTPUT_DIR}")


if __name__ == "__main__":
    generate_pdfs()
